// Package network реализует TCP/UDP-уровень сетевого взаимодействия
// с использованием Protocol Buffers.  Основные компоненты:
//   • TCPServerPB  – двоичный потоковый сервер, отвечающий за
//     надёжные сообщения и первоначальную авторизацию.
//   • UDPServerPB  – без соединений, используется для частых
//     непрерывных обновлений (позиции сущностей, пинг-pong).
//   • GameHandlerPB – игровая логика высокого уровня, транслирующая
//     сетевые сообщения в действия мира.
//   • GameServerPB  – фасад, объединяющий вышеописанные части и
//     запускающий игровой цикл.
//
// Пакет больше не содержит устаревших JSON-обработчиков – весь обмен
// данными происходит в компактном двоичном формате PB, что резко
// снижает нагрузку на сеть и ЦП.
package network
